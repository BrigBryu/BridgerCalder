package com.mygdx.game.util;

public class Zone {
    public int startX;
    public int startY;
    public int width;
    public int height;

    public Zone(int startX, int startY, int width, int height) {
        this.startX = startX;
        this.startY = startY;
        this.width = width;
        this.height = height;
    }
}